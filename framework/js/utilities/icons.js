/**
 * @file icons.js
 * @description Central registry for SVG icons.
 * Provides a lightweight, zero-dependency way to manage and render SVGs across the framework.
 * Default icons are sourced from Lucide (ISC/MIT).
 */

// Map pixel values to size class names
import { logger } from './logger.js';

const SIZE_MAP = {
    12: 'xs',
    16: 'sm',
    20: 'md',
    24: 'lg',
    32: 'xl',
    48: '2xl',
    64: '3xl'
};

class IconManager {
    constructor() {
        this.registry = new Map();

        // Register default system icons
        this.registerAll(DEFAULT_ICONS);
    }

    /**
     * Registers a single icon.
     * @param {string} name - The key name for the icon (e.g., 'menu').
     * @param {string} svgContent - The inner SVG content or full SVG string.
     */
    register(name, svgContent) {
        if (!name || !svgContent) return;
        this.registry.set(name, this._normalize(svgContent));
    }

    /**
     * Registers multiple icons at once.
     * @param {object} iconMap - Object where keys are icon names and values are SVG strings.
     */
    registerAll(iconMap) {
        if (!iconMap || typeof iconMap !== 'object') return;
        Object.entries(iconMap).forEach(([name, content]) => {
            this.register(name, content);
        });
    }

    /**
     * Resolves size option to a CSS class name.
     * Accepts: 'lg', 'md', 24, 20, etc.
     * @private
     */
    _resolveSize(size) {
        if (!size) return '';
        if (typeof size === 'string') return size;
        if (typeof size === 'number') return SIZE_MAP[size] || '';
        return '';
    }

    /**
     * Retrieves an icon by name.
     * @param {string} name - The name of the icon to retrieve.
     * @param {string|object} [options=''] - CSS class string OR options object.
     * @param {string} [options.class] - Additional CSS class.
     * @param {string|number} [options.size] - Size: 'xs'|'sm'|'md'|'lg'|'xl'|'2xl'|'3xl' OR pixels (12|16|20|24|32|48|64).
     * @param {number} [options.strokeWidth=2] - Stroke width.
     * @param {string} [options.color='currentColor'] - Stroke color.
     * @returns {string} The SVG string.
     */
    getIcon(name, options = '') {
        if (!name) return '';

        let className = '';
        let sizeClass = '';
        let strokeWidth = 2;
        let color = 'currentColor';

        // Handle arguments: string (CSS class shorthand) or object
        if (typeof options === 'string') {
            className = options;
        } else if (typeof options === 'object' && options !== null) {
            className = options.class || '';
            // Map size to CSS class (accepts string or number)
            const resolvedSize = this._resolveSize(options.size);
            if (resolvedSize) {
                sizeClass = `icon-${resolvedSize}`;
            }
            strokeWidth = options.strokeWidth !== undefined ? options.strokeWidth : 2;
            color = options.color || 'currentColor';
        }

        // If the icon is in our registry, return the SVG
        if (this.registry.has(name)) {
            const path = this.registry.get(name);
            const classes = ['icon', `icon-${name}`, sizeClass, className].filter(Boolean).join(' ');
            // No inline width/height - let CSS handle sizing via .icon and .icon-{size} classes
            return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="none" stroke="${color}" stroke-width="${strokeWidth}" stroke-linecap="round" stroke-linejoin="round" class="${classes}" aria-hidden="true">${path}</svg>`;
        }

        // Check if it's an emoji - warn but handle gracefully
        if (this._isEmoji(name)) {
            logger.warn(`[IconManager] Emoji "${name}" passed to getIcon(). Use getEmoji() instead.`);
            const size = typeof options === 'object' ? options.size : undefined;
            const extraClass = typeof options === 'object' ? options.class : (typeof options === 'string' ? options : '');
            return this.getEmoji(name, size, extraClass);
        }

        // Warn about unknown icons but don't block course loading - return a placeholder
        logger.warn(`[IconManager] Unknown icon: "${name}". Add it to DEFAULT_ICONS or course/icons.js.`);
        const classes = ['icon', `icon-${name}`, 'icon-missing', sizeClass, className].filter(Boolean).join(' ');
        // Return a simple question mark circle as placeholder
        return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="none" stroke="${color}" stroke-width="${strokeWidth}" stroke-linecap="round" stroke-linejoin="round" class="${classes}" aria-hidden="true"><circle cx="12" cy="12" r="10"/><path d="M9.09 9a3 3 0 0 1 5.83 1c0 2-3 3-3 3"/><path d="M12 17h.01"/></svg>`;
    }

    /**
     * Helper to strip <svg> tags if provided, storing only the inner content for flexibility.
     * @private
     */
    _normalize(content) {
        if (content.trim().startsWith('<svg')) {
            // Simple regex to extract inner content (robust enough for this controlled use case)
            const match = content.match(/<svg[^>]*>([\s\S]*?)<\/svg>/i);
            return match ? match[1] : content;
        }
        return content;
    }

    /**
     * Checks if a string is an emoji.
     * @private
     */
    _isEmoji(str) {
        if (!str || typeof str !== 'string') return false;
        // Detect common emoji unicode ranges
        const emojiRegex = /^[\u{1F300}-\u{1F9FF}\u{2600}-\u{26FF}\u{2700}-\u{27BF}\u{1F600}-\u{1F64F}\u{1F680}-\u{1F6FF}\u{1FA00}-\u{1FAFF}]/u;
        return emojiRegex.test(str);
    }

    /**
     * Returns a properly styled emoji span.
     * @param {string} emoji - The emoji character(s).
     * @param {string|number} [size] - Size: 'xs'|'sm'|'md'|'lg'|'xl'|'2xl'|'3xl' OR pixels.
     * @param {string} [extraClass=''] - Additional CSS classes.
     * @returns {string} HTML span element.
     */
    getEmoji(emoji, size, extraClass = '') {
        const resolvedSize = this._resolveSize(size);
        const sizeClass = resolvedSize ? `icon-${resolvedSize}` : '';
        const classes = ['emoji', sizeClass, extraClass].filter(Boolean).join(' ');
        return `<span class="${classes}" aria-hidden="true">${emoji}</span>`;
    }
}

